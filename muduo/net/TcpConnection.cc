void TcpConnection::send(const StringPiece& message)
{
  if (state_ == kConnected){
    if (loop_->isInLoopThread()){
      sendInLoop(message);
    }else{
      void (TcpConnection::*fp)(const StringPiece& message) = &TcpConnection::sendInLoop;
      loop_->runInLoop(
          std::bind(fp,
                    this,     // FIXME
                    message.as_string()));
                    //std::forward<string>(message)));
    }
  }
}

